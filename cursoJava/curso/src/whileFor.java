/**
 * whileFor
 */
public class whileFor {

    public static void main(String[] args) {
    
        for (int index = 5; index <=5; index++) {
            for (int i = 1; i <= index; i++) {
                System.out.print("*");
            }
                
            System.out.println();
        }

        
    }
}